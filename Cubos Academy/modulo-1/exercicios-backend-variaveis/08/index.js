let raioEmCentimetros = 1

const circunferenciaDoCirculo = 2 * Math.PI * raioEmCentimetros

const areaDoCirculo = Math.PI * raioEmCentimetros ** 2

console.log(`A circunferência do círculo é de ${circunferenciaDoCirculo.toFixed(2)} centimetros e a área do círculo é de ${areaDoCirculo.toFixed(2)} centimetros.`);